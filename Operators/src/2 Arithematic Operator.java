class arithematicOperator {
    public static void main(String[] args) {
        int a = 8;
        int b = 4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        float c = (float) a;
        System.out.println(c/3);
        System.out.println(c%3);
    }
}
